void	*ft_strrev(char *str)
{
	int i;
	int b;
	int swap;

	i = 0;
	b = 0;
	swap = 0;
	while (str[i])
		i++;
	i--;
	while (b < i)
	{
		swap = str[i];
		str[i] = str[b];
		str[b] = swap;
		b++;
		i--;
	}
	return (str);
}
